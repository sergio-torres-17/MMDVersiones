public class Parámetros {
    private int d,h,m,a,n0,c;

    public Parámetros() {
    }

    public Parámetros(int d, int h, int m, int a, int n0, int c) {
        this.d = d;
        this.h = h;
        this.m = m;
        this.a = a;
        this.n0 = n0;
        this.c = c;
    }

    public int getD() {
        return d;
    }

    public void setD(int d) {
        this.d = d;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getM() {
        return m;
    }

    public void setM(int m) {
        this.m = m;
    }

    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getN0() {
        return n0;
    }

    public void setN0(int n0) {
        this.n0 = n0;
    }

    public int getC() {
        return c;
    }

    public void setC(int c) {
        this.c = c;
    }
    


    
}
